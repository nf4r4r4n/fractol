#include <stdlib.h>
#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = (t_list *)malloc(sizeof(t_list));
	if (!lst)
		return (FT_NULL);
	lst->content = content;
	lst->next = FT_NULL;
	return (lst);
}
